// function expression(anonymus)
const sum = function (num1, num2) {
    return num1 + num2;
}

const result = sum(2, 8);
console.log(result);

// function expression
const sumTo = function sum1(num1, num2) {
    return num1 + num2;
}

const result1 = sumTo(2, 8);
console.log(result1);

// arrow function

const sum2 = (num1, num2) => num1 + num2;

const result2 = sum2(46, 8);
console.log(result2);